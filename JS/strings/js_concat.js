// JavaScript String concat() - joins two or more strings:
let txt = "Hello";
let txt1 = "Samuel!";
let txt2 = txt.concat(" ", txt1);

console.log(txt2);

// The concat() method can be used instead of the plus operator. These two lines do the same:
text1 = "Hello" + " " + "World!";
text2 = "Hello".concat(" ", "World!");

console.log(text1);
console.log(text2);