// Multiline strings: Template literals respect the line breaks in the source code, so you can write strings that span multiple lines like this:

const output = `I like the song. 
I gave it a score of 95%.`;

console.log(output)

// To have the equivalent output using a normal string you'd have to include line break characters (\n) in the string.

const output1 = "I like the song.\nI gave it a score of 90%.";
console.log(output1);


